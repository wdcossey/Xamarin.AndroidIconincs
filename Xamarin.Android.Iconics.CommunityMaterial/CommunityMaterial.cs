  public class CommunityMaterial : ITypeface
  {
    private const string TTF_FILE = "materialdesignicons-webfont.ttf";

    private static Typeface _typeface;

    private static Dictionary<string, char> _chars;

    public IIcon GetIcon(string key)
    {
      return new CommunityMaterialIcon((Icon) Enum.Parse(typeof (Icon), key, true));
    }

    public Dictionary<string, char> Characters
    {
      get
      {
        if (_chars == null)
        {
          _chars = new Dictionary<string, char>();

          foreach (var name in Enum.GetNames(typeof (Icon)))
          {
            var value = (Icon) Enum.Parse(typeof (Icon), name);
            _chars.Add(name, (char) value);
          }
        }

        return _chars;
      }
    }

    public string MappingPrefix
    {
      get { return "cmd"; }
    }

    public string FontName
    {
      get { return "Community Material Design"; }
    }

    public string Version
    {
      get { return "1.0.0"; }
    }

    public int IconCount
    {
      get { return _chars.Count; }
    }


    public ICollection<string> Icons
    {
      get { return Enum.GetNames(typeof (Icon)); }
    }

    public string Author
    {
      get { return "Templarian / Community / Google"; }
    }

    public string Url
    {
      get { return "http://materialdesignicons.com/"; }
    }

    public string Description
    {
      get
      {
        return
          "Material Design Icons are the official open-source icons featured in the Google Material Design specification.";
      }
    }

    public string License
    {
      get
      {
        return
          "Templates - Free, Community Icons - SIL Open Font License 1.1, Google Material Design Icons: Attribution 4.0 International";
      }
    }

    public string LicenseUrl
    {
      get { return "https://raw.githubusercontent.com/Templarian/MaterialDesign/master/license.txt"; }
    }

    public Typeface GetTypeface(Context context)
    {
      if (_typeface == null)
      {
        try
        {
          _typeface = Typeface.CreateFromAsset(context.Assets, /*"fonts/" +*/ TTF_FILE);
        }
        catch (Exception)
        {
          return null;
        }
      }
      return _typeface;
    }

